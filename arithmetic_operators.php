<?php
$first_num = 10;
$second_num = 5;


printf("%d + %d = %d\n", $first_num, $second_num, $first_num + $second_num);
printf("%d - %d = %d\n", $first_num, $second_num, $first_num - $second_num);
printf("%d x %d = %d\n", $first_num, $second_num, $first_num * $second_num);
printf("%d / %d = %d\n", $first_num, $second_num, $first_num / $second_num);
printf("%d  %d = %d\n", $first_num, $second_num, $first_num % $second_num);
printf("%d ** %d = %d\n", $first_num, $second_num, $first_num ** $second_num);

?>